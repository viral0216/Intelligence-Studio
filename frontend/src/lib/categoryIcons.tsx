import {
  Building2,
  AppWindow,
  Zap,
  Waves,
  FolderOpen,
  Library,
  BarChart3,
  Brain,
  ShieldCheck,
  Settings,
  TrendingUp,
  Users,
  Search,
  Share2,
  Sparkles,
  KeyRound,
  Store,
  Lock,
  Activity,
  Tag,
  Radio,
  Wallet,
  Landmark,
  Database,
  Network,
  Server,
  Receipt,
  type LucideIcon,
} from 'lucide-react'

const ICON_MAP: Record<string, LucideIcon> = {
  Building2,
  AppWindow,
  Zap,
  Waves,
  FolderOpen,
  Library,
  BarChart3,
  Brain,
  ShieldCheck,
  Settings,
  TrendingUp,
  Users,
  Search,
  Share2,
  Sparkles,
  KeyRound,
  Store,
  Lock,
  Activity,
  Tag,
  Radio,
  Wallet,
  Landmark,
  Database,
  Network,
  Server,
  Receipt,
}

export function CategoryIcon({ name, className }: { name: string; className?: string }) {
  const Icon = ICON_MAP[name]
  if (!Icon) return <span className={className}>{name}</span>
  return <Icon className={className || 'w-4 h-4'} />
}
